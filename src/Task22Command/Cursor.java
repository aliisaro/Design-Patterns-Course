package Task22Command;

public class Cursor {
  private int x; // X-coordinate of the cursor
  private int y; // Y-coordinate of the cursor

  public Cursor(int x, int y) {
    this.x = x;
    this.y = y;
  }

  public int getX() {
    return x;
  }

  public void setX(int x) {
    this.x = x;
  }

  public int getY() {
    return y;
  }

  public void setY(int y) {
    this.y = y;
  }
}
